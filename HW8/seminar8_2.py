import json


def uniq_id(data: dict, id: str) -> bool:
    """
    —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –Ω–∞ —É–Ω–∏–∫–∞–ª—å–Ω–æ—Å—Ç—å id
    :param data: —Å–ª–æ–≤—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏
    :param id: id –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
    :return:
    """
    for item in data.values():
        if id in item.keys():
            return False
    return True


def enter_id_name(name_file: str) -> None:
    """
    aey–∫—Ü–∏—è —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç json —Å –∑–∞–ø–∏—Å—å—é –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –≤ —Ñ–∞–π–ª
    :param name_file: –∏–º—è –≤—ã—Ö–æ–¥–Ω–æ–≥–æ —Ñ–∞–π–ª–∞
    :return:
    """
    name_file += ".json"

    while True:
        id = input("id: ")
        name = input("name: ")
        level = input("level: ")

        try:
            with open(name_file, 'r', encoding='utf-8') as fr:
                read_dict: dict = json.load(fr)

        except FileNotFoundError:
            read_dict: dict = {str(i): {} for i in range(1, 8, 1)}
        if uniq_id(read_dict, id):
            read_dict[level].update({id: name})
        else:
            print('–Ω–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–π id')
            continue

        with open(name_file, 'w', encoding='utf-8') as fw:
            json.dump(read_dict, fw, indent=2)


